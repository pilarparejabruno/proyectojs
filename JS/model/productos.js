class Producto {
  nombre;
  categoria;
  descripcion;
  marca;
  precio;
  id;

  constructor(nombre, categoria, descripcion, marca, precio, id) {
    this.nombre = nombre;
    this.categoria = categoria;
    this.descripcion = descripcion;
    this.marca = marca;
    this.precio = precio;
    this.id = id;
  }
}
